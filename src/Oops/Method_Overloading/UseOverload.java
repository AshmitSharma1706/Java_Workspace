package Oops.Method_Overloading;

public class UseOverload {
    public static void main(String[] args) {
        Overload o=new Overload();
        o.show(10);
        o.show(10.1);
        o.show("Ashmit");
        o.show('A');
        o.show("A");
        o.show('a');
        o.show("a");
        o.show(1.9f);
    }
}
