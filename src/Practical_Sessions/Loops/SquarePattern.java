package Practical_Sessions.Loops;

public class SquarePattern {
    public static void main(String[] args) {
        for(int i=1; i<=1; i++){
            for(int j=5; j>=1; j--){
                System.out.print("*  ");
            }
            System.out.println();
        }
        for(int i=1; i<=3; i++){
            for(int j=3; j>=3; j--){
                System.out.print("*  ");
            }
            for(int j=3; j>=1; j--){
                System.out.print("   ");
            }
            for(int j=3; j>=3; j--){
                System.out.print("*  ");
            }
            System.out.println();
        }
        for(int i=1; i<=1; i++){
            for(int j=5; j>=1; j--){
                System.out.print("*  ");
            }
            System.out.println();
        }
    }
}
